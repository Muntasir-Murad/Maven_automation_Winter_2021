package Day2_03072021;

public class WhileLoop {
    public static void main(String[] args) {

        String [] states;
        states= new String[4];
        states[0]= "New York";
        states[1]= "New Jersey";
        states[2]= "Pennsylvania";
        states[3]= "Florida";

      //start while loop

        int i=0;

        while (i< states.length) {
            System.out.println("My state is " + states[i]);

            i=i+1;

        }









    } //end of main   }
}//end of class
